/**
  * @brief  The application entry point.
  *
  * @retval None
  */
int main(void)
{
  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* Configure the system clock */
  SystemClock_Config();

  /* Initialize all configured peripherals */
  MX_GPIO_Init();

  /* =============Initialization=============== */
  ILI9341_Init();
  ILI9341_Set_Rotation(0);
  HAL_Delay(500);
  ILI9341_Fill_Screen(RED);
  HAL_Delay(500);
  ILI9341_Fill_Screen(GREEN);
  HAL_Delay(500);
  ILI9341_Fill_Screen(BLUE);
  HAL_Delay(500);
  ILI9341_Fill_Screen(BLACK);
  HAL_Delay(500);

  /* ===============Draw menu================= */
  Display_Menu();

  uint16_t xtemp, ytemp;

  TP_Init();
  /* Infinite loop */
  while (1)
  {

	  tp_dev.scan(0);
	  if(tp_dev.sta & TP_PRES_DOWN)
	  {
		  xtemp = TP_Read_XOY(0xD0);  // dec..208
		  ytemp = TP_Read_XOY(0x90);  // dec..144

		  if(0 == state)
		  {
			  if(xtemp > 2700 && xtemp < 3400 && ytemp > 3000 && ytemp < 3500) {
				  Display_Color_Picture();
				  state = 1;
			  }

			  if(xtemp > 1200 && xtemp < 1900 && ytemp > 3000 && ytemp < 3500) {
				  Display_Text();
				  state = 1;
			  }
		  }
		  else
		  {
			  if(xtemp > 3400 && xtemp < 3900 && ytemp > 350 && ytemp < 750) {
				  Display_Menu();
				  state = 0;
			  }
		  }

		  HAL_Delay(500);
	  }

  }

}

/**
  * @brief System Clock Configuration
  * @retval None
  */
void SystemClock_Config(void)
{

  RCC_OscInitTypeDef RCC_OscInitStruct;
  RCC_ClkInitTypeDef RCC_ClkInitStruct;

    /**Configure the main internal regulator output voltage 
    */
  __HAL_RCC_PWR_CLK_ENABLE();

  __HAL_PWR_VOLTAGESCALING_CONFIG(PWR_REGULATOR_VOLTAGE_SCALE1);

    /**Initializes the CPU, AHB and APB busses clocks 
    */
  RCC_OscInitStruct.OscillatorType = RCC_OSCILLATORTYPE_HSE; // _HSI
  RCC_OscInitStruct.HSEState = RCC_HSE_ON;  // HSI -> HSE
  //RCC_OscInitStruct.HSICalibrationValue = 16;
  RCC_OscInitStruct.PLL.PLLState = RCC_PLL_ON;
  RCC_OscInitStruct.PLL.PLLSource = RCC_PLLSOURCE_HSE; // HSI...
  RCC_OscInitStruct.PLL.PLLM = 8;
  RCC_OscInitStruct.PLL.PLLN = 336; // originally 192;
  RCC_OscInitStruct.PLL.PLLP = RCC_PLLP_DIV2;  // originally DIV4...
  RCC_OscInitStruct.PLL.PLLQ = 7; // originally 8;
  if (HAL_RCC_OscConfig(&RCC_OscInitStruct) != HAL_OK)
  {
    _Error_Handler(__FILE__, __LINE__);
  }

    /**Initializes the CPU, AHB and APB busses clocks 
    */
  RCC_ClkInitStruct.ClockType = RCC_CLOCKTYPE_HCLK|RCC_CLOCKTYPE_SYSCLK
                              |RCC_CLOCKTYPE_PCLK1|RCC_CLOCKTYPE_PCLK2;
  RCC_ClkInitStruct.SYSCLKSource = RCC_SYSCLKSOURCE_PLLCLK;
  RCC_ClkInitStruct.AHBCLKDivider = RCC_SYSCLK_DIV1;
  RCC_ClkInitStruct.APB1CLKDivider = RCC_HCLK_DIV4;
  RCC_ClkInitStruct.APB2CLKDivider = RCC_HCLK_DIV2;

  if (HAL_RCC_ClockConfig(&RCC_ClkInitStruct, FLASH_LATENCY_3) != HAL_OK)
  {
    _Error_Handler(__FILE__, __LINE__);
  }

    /**Configure the Systick interrupt time 
    */
  HAL_SYSTICK_Config(HAL_RCC_GetHCLKFreq()/1000);

    /**Configure the Systick 
    */
  HAL_SYSTICK_CLKSourceConfig(SYSTICK_CLKSOURCE_HCLK);

  /* SysTick_IRQn interrupt configuration */
  HAL_NVIC_SetPriority(SysTick_IRQn, 0, 0);
}
